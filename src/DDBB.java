public class DDBB {
    private String port;
    private String ip;
    private String dbName;
    private String username;
    private String password;

    public DDBB(String port, String ip, String dbName, String username, String password) {
        this.port = port;
        this.ip = ip;
        this.dbName = dbName;
        this.username = username;
        this.password = password;
    }

}
